void test_time_conversions()
{
    struct timespec tm;
    time_t current_unix_time = 0, current_weekly_time = 0;
    time_t expected_unix_time = 0, expected_weekly_time = 0;

    if( 0 == clock_gettime(CLOCK_REALTIME, &tm) ) {
        current_unix_time = tm.tv_sec;

        current_weekly_time = convert_unix_time_to_weekly(current_unix_time);
        expected_unix_time  = convert_weekly_time_to_unix(current_weekly_time);
        CU_ASSERT(expected_unix_time == current_unix_time);

        expected_weekly_time = convert_unix_time_to_weekly(expected_unix_time);
        CU_ASSERT(expected_weekly_time == current_weekly_time);
    } else {
        CU_FAIL("Current time unavailable!!!\n");
    }
}

void add_suites( CU_pSuite *suite )
{
    printf("--------Start of Test Cases Execution ---------\n");
    *suite = CU_add_suite( "tests", NULL, NULL );
    CU_add_test( *suite, "Test 1", test_time_conversions );
}

/*----------------------------------------------------------------------------*/
/*                             External Functions                             */
/*----------------------------------------------------------------------------*/
int main( void )
{
    unsigned rv = 1;
    CU_pSuite suite = NULL;

    if( CUE_SUCCESS == CU_initialize_registry() ) {
        add_suites( &suite );

        if( NULL != suite ) {
            CU_basic_set_mode( CU_BRM_VERBOSE );
            CU_basic_run_tests();
            printf( "\n" );
            CU_basic_show_failures( CU_get_failure_list() );
            printf( "\n\n" );
            rv = CU_get_number_of_tests_failed();
        }

        CU_cleanup_registry();

    }

    return rv;
}

